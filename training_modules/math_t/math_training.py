import math

print(math.sqrt(4)) # квадратный корень
print(math.factorial(4)) # факториал
print(math.ceil(4.5)) # округление в большую сторону
print(math.floor(10.2)) # окгругление в меньшую сторону
print(math.exp(5)) # e^x
print(math.log(10,2.3)) # логарифм
print(math.pow(2,3)) # x^y
print(math.sin(10)) # sin
print(math.cos(10)) # cos
print(math.tan(10)) # tan
print(math.pi) # число pi
print(math.e) # число e